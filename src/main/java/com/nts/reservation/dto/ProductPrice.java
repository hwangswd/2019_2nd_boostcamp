package com.nts.reservation.dto;

/**
 * 상품 가격
 * @author 황성욱
 * @since 2019-07-30
 */
public class ProductPrice {
	private int productPriceId; // 상품 가격 Id
	private int productId; // 상품 Id
	private String priceTypeName; // 가격 타입명
	private int price; // 가격
	private double discountRate; //	할인율
	private String createDate; // 생성일 ($date-time)
	private String modifyDate; // 수정일 ($date-time)

	public int getProductPriceId() {
		return productPriceId;
	}

	public void setProductPriceId(int productPriceId) {
		this.productPriceId = productPriceId;
	}

	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}

	public String getPriceTypeName() {
		return priceTypeName;
	}

	public void setPriceTypeName(String priceTypeName) {
		this.priceTypeName = priceTypeName;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public double getDiscountRate() {
		return discountRate;
	}

	public void setDiscountRate(double discountRate) {
		this.discountRate = discountRate;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getModifyDate() {
		return modifyDate;
	}

	public void setModifyDate(String modifyDate) {
		this.modifyDate = modifyDate;
	}

	@Override
	public String toString() {
		return "ProductPrice [productPriceId=" + productPriceId + ", productId=" + productId + ", priceTypeName="
			+ priceTypeName + ", price=" + price + ", discountRate=" + discountRate + ", createDate=" + createDate
			+ ", modifyDate=" + modifyDate + "]";
	}
}
